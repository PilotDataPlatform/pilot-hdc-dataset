from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware


def setup_cors_middlware(app: FastAPI) -> None:  # pragma: no cover
    app.add_middleware(
        CORSMiddleware,
        allow_origins='*',
        allow_credentials=True,
        allow_methods=['*'],
        allow_headers=['*'],
    )


middlewares = (setup_cors_middlware,)
